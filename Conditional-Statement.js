//conditional statements
//if statements //else statement
let age = 45;
if (age > 18) {
    console.log("you can vote");
} else {
    console.log("you cannot vote ");
}
//odd or even using conditional statement
let a = 7;
if (a % 2 == 0) {
    console.log("even");
} else {
    console.log("odd");
}
//else if statements
let a = 35;
if (a < 18) {
    console.log("Junior");
} else if (a > 50) {
    console.log("senior");
} else {
    console.log("minor");
}
//ternary operator
let age = 28;
let res = age > 18 ? "adults" : "children";
console.log(res);
